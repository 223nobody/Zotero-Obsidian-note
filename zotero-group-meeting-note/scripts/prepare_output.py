#!/usr/bin/env python3
"""Prepare deterministic output paths for Zotero group-meeting notes.

This script intentionally handles only mechanical filesystem work:
filename sanitization, default directory creation, optional note stubs,
and asset copying. The note body should still be generated by Codex.
"""

